#include "libft.h"

int						ft_atoi(const char *str)
{
	int					nb;
	int					i;
	int					neg;

	i = 0;
	neg = 1;
	nb = 0;
	while (str[i] == ' ' || (str[i] >= 7 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
		neg = 44 - str[i++];
	while (str[i] >= '0' && str[i] <= '9')
		nb = (nb * 10) + (str[i++] - '0');
	return (nb * neg);
}
